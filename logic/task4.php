<?php
$a = [
    [3, 7, 2, 10, 1],
    [6, 8, 2, 29, 7],
    [17, 15, 13, 8, 12],
    [6, -3, 1, 2, 9],
    [7, 8, 5, 1, -2],
];

$max = 0;
$n = count($a);
for ($i = 0; $i < $n; $i++) {
    for ($j = 0; $j < $n; $j++) {
        if(($i == $j || ($n - 1 - $i) == $j) && $a[$i][$j] > $max)
            $max = $a[$i][$j];
    }
}

echo 'MAX = '. $max;